package model;

public class ConclusionEntity {
	
	private String sessionId;
	
	private String lv1ConId;
	
	private String lv1ConName;
	
	private String lv2ConId;
	
	private String lv2ConName;
	
	private String conNum;

	public String getSessionId() {
		return sessionId;
	}

	public void setSessionId(String sessionId) {
		this.sessionId = sessionId;
	}

	public String getLv1ConId() {
		return lv1ConId;
	}

	public void setLv1ConId(String lv1ConId) {
		this.lv1ConId = lv1ConId;
	}

	public String getLv1ConName() {
		return lv1ConName;
	}

	public void setLv1ConName(String lv1ConName) {
		this.lv1ConName = lv1ConName;
	}

	public String getLv2ConId() {
		return lv2ConId;
	}

	public void setLv2ConId(String lv2ConId) {
		this.lv2ConId = lv2ConId;
	}

	public String getLv2ConName() {
		return lv2ConName;
	}

	public void setLv2ConName(String lv2ConName) {
		this.lv2ConName = lv2ConName;
	}

	public String getConNum() {
		return conNum;
	}

	public void setConNum(String conNum) {
		this.conNum = conNum;
	}
	
}
